#include "lcd.h"

// ---------------------------------------------------------------------------
// Interface avec l'afficheur LCD
// ---------------------------------------------------------------------------
LcdCL* LcdCL::lcd_=NULL;

// ---------------------------------------------------------------------------
// Lcd::Lcd
// ---------------------------------------------------------------------------
LcdCL::LcdCL():
  RobotDeviceCL("Lcd", CLASS_LCD) 
{
  assert(!lcd_);
  lcd_=this;
}

// ---------------------------------------------------------------------------
// Lcd::Lcd
// ---------------------------------------------------------------------------
LcdCL::~LcdCL()
{
  lcd_ = NULL;
}


