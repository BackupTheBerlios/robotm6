

#include "lcd.h"

#include <assert.h>
#include <stdarg.h>
#include <stdio.h>


// ---------------------------------------------------------------------------
// Interface avec l'afficheur LCD
// ---------------------------------------------------------------------------
LcdCL* LcdCL::lcd_=NULL;

// ---------------------------------------------------------------------------
// Lcd::Lcd
// ---------------------------------------------------------------------------
LcdCL::LcdCL():
  RobotIODevice("Lcd", CLASS_LCD) 
{
  assert(!lcd_);
  lcd_=this;
}

// ---------------------------------------------------------------------------
// Lcd::Lcd
// ---------------------------------------------------------------------------
LcdCL::~LcdCL()
{
  lcd_ = NULL;
}


