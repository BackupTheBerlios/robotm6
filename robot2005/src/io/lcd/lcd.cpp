

#include "lcd.h"

#include <assert.h>
#include <stdarg.h>
#include <stdio.h>


// ---------------------------------------------------------------------------
// Interface avec l'afficheur LCD
// ---------------------------------------------------------------------------
Lcd* Lcd::lcd_=NULL;

// ---------------------------------------------------------------------------
// Lcd::Lcd
// ---------------------------------------------------------------------------
Lcd::Lcd():
  RobotIODevice("Lcd", CLASS_LCD) 
{
  assert(!lcd_);
  lcd_=this;
}

// ---------------------------------------------------------------------------
// Lcd::Lcd
// ---------------------------------------------------------------------------
Lcd::~Lcd()
{
  lcd_ = NULL;
}


